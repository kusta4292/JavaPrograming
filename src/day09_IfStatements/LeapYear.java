public class LeapYear {

    public static void main(String[] args) {

        int year = 2005;

        if (year%4 ==0){

            System.out.println("Year " + year+ " is the leap year");

            }
        else{
            System.out.println("Year " + year + " is not a leap year");

        }



















    }
}
